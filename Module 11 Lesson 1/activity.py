a = 19
b = 15

#And operator
and_result = a & b
print("The result of the AND Operator is ", and_result)

#OR operator
or_result = a | b
print("The result of the OR Operator is ", or_result)

#XOR operator
xor_result = a ^ b
print("The result of the XOR Operator is ", xor_result)

#NOT operator
not_result = ~a
print("The result of the NOT Operator is ", not_result)